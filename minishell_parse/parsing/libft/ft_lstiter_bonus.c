#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list *p;

	if (lst && f)
	{
		p = lst;
		while (p != NULL)
		{
			f(p->content);
			p = p->next;
		}
	}
}
